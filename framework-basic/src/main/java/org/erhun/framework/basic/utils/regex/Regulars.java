package org.erhun.framework.basic.utils.regex;

import java.util.regex.Pattern;

/**
 * @author gorilla@aliyun.com
 */
public interface Regulars {

	/**
	 * 简单手机号
	 */
	String REGEX_MOBILE_SIMPLE = "^[1]\\d{10}$";

	/**
	 * <p>
	 * 移动：134(0-8)、135、136、137、138、139、147、150、151、152、157、158、159、178、182、183、184、187、188、198
	 * </p>
	 * <p>
	 * 联通：130、131、132、145、155、156、166、171、175、176、185、186
	 * </p>
	 * <p>
	 * 电信：133、153、173、177、180、181、189、199
	 * </p>
	 * <p>
	 * 全球星：1349
	 * </p>
	 * <p>
	 * 虚拟运营商：170
	 * </p>
	 */
	String REGEX_MOBILE = "^(13[0-9]|14[579]|15[0-3,5-9]|16[6]|17[0135678]|18[0-9]|19[89])\\d{8}$";

	/**
	 * 固定电话号码，可带区号，然后6至少8位数字
	 */
	String REGEX_TEL = "^(\\d{3,4}-)?\\d{6,8}$";

	/**
	 * 份证号码15位
	 */
	String REGEX_ID_CARD_15 = "^[1-9]\\d{7}((0\\d)|(1[0-2]))(([0|1|2]\\d)|3[0-1])\\d{3}$";

	/**
	 * 身份证号码18位
	 */
	String REGEX_ID_CARD_18 = "^[1-9]\\d{5}[1-9]\\d{3}((0\\d)|(1[0-2]))(([0|1|2]\\d)|3[0-1])\\d{3}([0-9Xx])$";

	/**
	 * 邮箱
	 */
	String REGEX_EMAIL = "^\\w+([-+.]\\w+)*@\\w+([-.]\\w+)*\\.\\w+([-.]\\w+)*$";

	/**
	 * URL
	 */
	String REGEX_URL = "[a-zA-z]+://[^\\s]*";

	/**
	 * yyyy-MM-dd
	 */
	String REGEX_DATE = "^(?:(?!0000)[0-9]{4}-(?:(?:0[1-9]|1[0-2])-(?:0[1-9]|1[0-9]|2[0-8])|(?:0[13-9]|1[0-2])-(?:29|30)|(?:0[13578]|1[02])-31)|(?:[0-9]{2}(?:0[48]|[2468][048]|[13579][26])|(?:0[48]|[2468][048]|[13579][26])00)-02-29)$";

	/**
	 * IP地址
	 */
	String REGEX_IP = "((2[0-4]\\d|25[0-5]|[01]?\\d\\d?)\\.){3}(2[0-4]\\d|25[0-5]|[01]?\\d\\d?)";
}

